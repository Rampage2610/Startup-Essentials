public class UserInformation {
    public String name;
    public String phone;
    public String email;
    public String subject;
    public String message;



    public UserInformation() {

    }

    public UserInformation(String name, String phone, String email, String subject, String message) {
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.subject = subject;
        this.message = message;
    }
}


